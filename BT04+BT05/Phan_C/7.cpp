// Hàm vẽ từng chữ cái lớn
void drawLargeCharacter(char letter) {
    // Chuyển đổi ký tự thành chữ hoa
    letter = toupper(letter);
    
    // Kiểm tra xem ký tự có trong map không
    if (characterShapes.find(letter) != characterShapes.end()) {
        // In ra hình dạng tương ứng với ký tự
        vector<string> shape = characterShapes[letter];
        for (const string& line : shape) {
            cout << line << " ";
        }
    } else {
        cout << "Invalid character!";
    }
}

int main() {
    string word;
    
    cout << "Nhap tu tieng Anh (khong qua 12 chu cai): ";
    cin >> word;

    for (char c : word) {
        drawLargeCharacter(c);
        cout << " ";
    }
    
    return 0;
}
